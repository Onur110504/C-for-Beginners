Console.WriteLine("For loop:");
for (int i = 1; i <= 5; i++)
{
    Console.WriteLine("i = " + i);
}

Console.WriteLine("\nWhile loop:");
int j = 1;
while (j <= 5)
{
    Console.WriteLine("j = " + j);
    j++;
}

Console.WriteLine("\nDo-while loop:");
int k = 1;
do
{
    Console.WriteLine("k = " + k);
    k++;
} while (k <= 5);
